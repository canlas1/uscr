><H4 

><B>&sect; 626<B>. 	Disclosures to FBI for counterintelligence purposes</B> [15 U.S.C. &sect; 1681u] </H4
><P 

>(a) <I>Identity of financial institutions. </I>Notwithstanding section 604 [&sect; 1681b] or any other provision of this title, a consumer reporting agency shall furnish to the Federal Bureau of Investigation the names and addresses of all financial institutions (as that term is defined in section 1101 of the Right to Financial Privacy Act of 1978 [12 </P
><P 

>U.S.C. &sect; 3401]) at which a consumer maintains or has maintained an account, to the extent that information is in the files of the agency, when presented with a written request for that information, signed by the Director of the Federal Bureau of Investigation, or the Director's designee in a position not lower than Deputy Assistant Director at Bureau headquarters or a Special Agent in Charge of a Bureau field office designated by the Director, which certifies compliance with this section. The Director or the Director's designee may make such a certification only if the Director or the Director's designee has determined in writing, that such information is sought for the conduct of an authorized investigation to protect against international terrorism or clandestine intelligence activities, provided that such an investigation of a United States person is not conducted solely upon the basis of activities protected by the first amendment to the Constitution of the United States. </P
><DL 

><DT 

>(b) </DT
><DD 

><I>Identifying information.</I> Notwithstanding the provisions of section 604 [&sect; 1681b] or any other provision of this title, a consumer reporting agency shall furnish identifying information respecting a consumer, limited to name, address, former addresses, places of employment, or former places of employment, to the Federal Bureau of Investigation when presented with a written request, signed by the Director or the Director's designee, which certifies compliance with this subsection. The Director or the Director's designee in a position not lower than Deputy Assistant Director at Bureau headquarters or a Special Agent in Charge of a Bureau field office designated by the Director may make such a certification only if the Director or the Director's designee has determined in writing that such information is sought for the conduct of an authorized investigation to protect against international terrorism or clandestine intelligence activities, provided that such an investigation of a United States person is not conducted solely upon the basis of activities protected by the first amendment to the Constitution of the United States. </DD
><DT 

>(c) </DT
><DD 

><I>Court order for disclosure of consumer reports.</I> Notwithstanding section 604 [&sect; 1681b] or any other provision of this title, if requested in writing by the Director of the Federal Bureau of Investigation, or a designee of the Director in a position not lower than Deputy Assistant Director at Bureau headquarters or a Special Agent in Charge of a Bureau field office designated by the Director, a court may issue an order ex parte directing a consumer reporting agency to furnish a consumer report to the Federal Bureau of Investigation, upon a showing in camera that the consumer report is sought for the conduct of an authorized investigation to protect against international terrorism or clandestine intelligence activities, provided that such an investigation of a United States person is not conducted solely upon the basis of activities protected by the first amendment to the Constitution of the United States. The terms of an order issued under this subsection shall not disclose that the order is issued for purposes of a counterintelligence investigation. </DD
><DT 

>(d) </DT
><DD 

><I>Confidentiality.</I> No consumer reporting agency or officer, employee, or agent of a consumer reporting agency shall disclose to any person, other than those officers, employees, or agents of a consumer reporting agency necessary to fulfill the requirement to disclose information to the Federal Bureau of Investigation under this section, that the Federal Bureau of Investigation has sought or obtained the identity of financial institutions or a consumer report respecting any consumer under subsection (a), (b), or (c), and no consumer reporting agency or officer, employee, or agent of a consumer reporting agency shall include in any consumer report any information that would indicate that the Federal Bureau of Investigation has sought or obtained such information or a consumer report. </DD
><DT 

>(e) </DT
><DD 

><I>Payment of fees.</I> The Federal Bureau of Investigation shall, subject to the availability of appropriations, pay to the consumer reporting agency assembling or providing report or information in accordance with procedures established under this section a fee for reimbursement for such costs as are reasonably necessary and which have been directly incurred in searching, reproducing, or transporting books, papers, records, or other data required or requested to be produced under this section. </DD
><DT 

>(f</B>) 	</DT
><DD 

><I>Limit on dissemination.</I> The Federal Bureau of Investigation may not disseminate information obtained pursuant to this section outside of the Federal Bureau of Investigation, except to other Federal agencies as may be necessary for the approval or conduct of a foreign counterintelligence investigation, or, where the information concerns a person subject to the Uniform Code of Military Justice, to appropriate investigative authorities within the military department concerned as may be necessary for the conduct of a joint foreign counterintelligence investigation. </DD
><DT 

>(g) </DT
><DD 

><I>Rules of construction. </I>Nothing in this section shall be construed to prohibit information from being furnished by the Federal Bureau of Investigation pursuant to a subpoena or court order, in connection with a judicial or administrative proceeding to enforce the provisions of this Act. Nothing in this section shall be construed to authorize or permit the withholding of information from the Congress. </DD
><DT 

>(h) </DT
><DD 

><I>Reports to Congress. </I>On a semiannual basis, the Attorney General shall fully inform the Permanent Select Committee on Intelligence and the Committee on Banking, Finance and Urban Affairs of the House of Representatives, and the Select Committee on Intelligence and the Committee on Banking, Housing, and Urban Affairs of the Senate concerning all requests made pursuant to subsections (a), (b), and (c). </DD
><DL 

><DT 

>(i) 	</DT
><DD 

><I>Damages. </I>Any agency or department of the United States obtaining or disclosing any consumer reports, records, or information contained therein in violation of this section is liable to the consumer to whom such consumer reports, records, or information relate in an amount equal to the sum of </DD
><DL 

><DT 

>(1) </DT
><DD 

>$100, without regard to the volume of consumer reports, records, or information involved; </DD
><DT 

>(2) </DT
><DD 

>any actual damages sustained by the consumer as a result of the disclosure; </DD
><DT 

>(3) </DT
><DD 

>if the violation is found to have been willful or intentional, such punitive damages as a court may allow; and </DD
><DT 

>(4) </DT
><DD 

>in the case of any successful action to enforce liability under this subsection, the costs of the action, together with reasonable attorney fees, as determined by the court.</DD
></DL
></DL
><DL 

><DT 

> (j) 	</DT
><DD 

><I>Disciplinary actions for violations. </I>If a court determines that any agency or department of the United States has violated any provision of this section and the court finds that the circumstances surrounding the violation raise questions of </DD
>whether or not an officer or employee of the agency or department acted willfully or intentionally with respect to the violation, the agency or department shall promptly initiate a proceeding to determine whether or not disciplinary action is warranted against the officer or employee who was responsible for the violation.</DL
><DT 

> (k) </DT
><DD 

><I>Good-faith exception.</I> Notwithstanding any other provision of this title, any consumer reporting agency or agent or employee thereof making disclosure of consumer reports or identifying information pursuant to this subsection in good-faith reliance upon a certification of the Federal Bureau of Investigation pursuant to provisions of this section shall not be liable to any person for such disclosure under this title, the constitution of any State, or any law or regulation of any State or any political subdivision of any State. </DD
><DT 

>(l) 	</DT
><DD 

><I>Limitation of remedies.</I> Notwithstanding any other provision of this title, the remedies and sanctions set forth in this section shall be the only judicial remedies and sanctions for violation of this section. </DD
><DT 

>(m) 	</DT
><DD 

><I>Injunctive relief</I>. In addition to any other remedy contained in this section, injunctive relief shall be available to require compliance with the procedures of this section. In the event of any successful action under this subsection, costs together with reasonable attorney fees, as determined by the court, may be recovered. </DD
