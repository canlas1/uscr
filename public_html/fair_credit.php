><H4 

><B>&sect; 601. Short title </H4
><P 

></B>This title may be cited as the &ldquo;Fair Credit Reporting Act&rdquo;. <B>&sect; 602</B>. <B>Congressional findings and statement of purpose</B> [15 U.S.C. &sect; 1681] </P
><DL 

><DL 

><DT 

>(a) </DT
><DD 

><I>Accuracy and fairness of credit reporting.</I> The Congress makes the following findings: </DD
><DL 

><DT 

>(1) </DT
><DD 

>The banking system is dependent upon fair and accurate credit reporting. Inaccurate credit reports directly impair the efficiency of the banking system, and unfair credit reporting methods undermine the public confidence which is essential to the continued functioning of the banking system. </DD
><DT 

>(2) </DT
><DD 

>An elaborate mechanism has been developed for investigating and evaluating the credit worthiness, credit standing, credit capacity, character, and general reputation of consumers. </DD
><DT 

>(3) </DT
><DD 

>Consumer reporting agencies have assumed a vital role in assembling and evaluating consumer credit and other information on consumers. </DD
><DT 

>(4) </DT
><DD 

>There is a need to insure that consumer reporting agencies exercise their grave responsibilities with fairness, impartiality, and a respect for the consumer's right to privacy. </DD
></DL
></DL
><DT 

>(b) </DT
><DD 

><I>Reasonable procedures.</I> It is the purpose of this title to require that consumer reporting agencies adopt reasonable procedures for meeting the needs of commerce for consumer credit, personnel, insurance, and other information in a manner which is fair and equitable to the consumer, with regard to the confidentiality, accuracy, relevancy, and proper utilization of such information in accordance with the requirements of this title. </DD
></DL
