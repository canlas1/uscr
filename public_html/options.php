        	  <p><a name="minimum" id="minimum"></a><strong>Paying The  Minimum On Your Credit Cards</strong> <br />
        	    Paying only your monthly minimum payments is by far the slowest way to eliminate your credit card debt.  It could take decades before your debt is completely paid off.  Many people never pay off their debt.  Even on a low interest credit card, much of your minimum payment could be going towards monthly interest charges, so you are not making a significant dent on what you actually owe.  Paying the minimum does decrease your balance over time, but it will cost you much more money and much more time than our debt negotiation plans.</p>
