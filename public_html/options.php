        	  <p><a name="consolidation" id="consolidation"></a><strong>Debt  Consolidation Loans</strong> <br />
        	    Consolidation loans involve borrowing money to get out of debt.  A new loan is required to pay off the full amount of your existing accounts.  You will pay back the new loan plus interest over a fixed period of time.  People often choose this option to secure a lower overall interest rate and one convenient monthly payment.  However, the appeal of a slightly lower interest rate and a reduced single monthly payment can result in taking on a loan that requires a lengthy period of time to payoff.  This ultimately means you will pay considerable finance charges over the full course of the consolidation loan.  Furthermore, some types of consolidation loans can involve the use of collateral, such as a home, to secure the loan.  For many people this is simply not an option or can represent yet another financial risk down the road. </p>
        	          	    <A href="#top">Back to Top</A>

        	  <p><a name="counseling" id="counseling"></a><strong>Credit  Counseling</strong> <br />
        	   Credit counseling services negotiate with the credit card companies in an effort to reduce your interest rates and minimum monthly payments.  In order to participate in such programs, you generally must close your credit card accounts and pay back the full amount plus interest.  This type of debt repayment can typically take 4 to 7 years to completely pay off your creditors.  Your new minimum monthly payment may not be much lower and could be even higher than your original minimum payment due to the structure of your plan.  This obviously won't benefit you or your financial situation if you are already burdened with monthly payments that you cannot afford.  You may even find that you can't get your interest rate significantly lowered from your current credit card rates, or one or more of your credit card companies may choose not to participate in this type of program. </p>
      	    <A href="#top">Back to Top</A>

      	           	      <p><a name="bankruptcy" id="bankruptcy"></a><strong>Bankruptcy</strong> <br />
        	  Bankruptcy is the most severe solution and is universally considered a last resort.  It remains on your credit report anywhere from 7 to 10 years and it is considered public record.  This means it could be harder for you to get a loan, mortgage, and even a job in the future.  Banks and potential employers can and do check your credit report before lending you money or offering employment.  Ultimately, bankruptcy is a personal decision that should be weighed heavily against other options and is best explored with the assistance of an attorney.  In additional to any legal costs associated with consulting an attorney, bankruptcy can cost significantly more in legal fees to proceed with the process.</p>
        	  
